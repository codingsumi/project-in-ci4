                    <h2>DPS-N Anti Ragging Ammendment</h2>
                    <p class="title">Zero Tolerance</p>
                    <p>D. P. Singh Institute of Education (nursing) has adopted ‘Zero Tolerance’ Policy regarding any
                        incident or complaint of ragging in the institute or its hostel.

                        Any one or more of the following act, as per the UGC Regulations, is considered an act of
                        ragging in the Institute:- </p>
                    <p>
                        a) Any conduct by any student or students whether by words spoken or written or by an act which
                        has the effect of teasing, treating or handling with rudeness a fresher or any other student.
                    </p>
                    <p>
                        b) Indulging in rowdy or indisciplined activities by any student or students which causes or is
                        likely to cause annoyance, hardship, physical or psychological harm or to raise fear or
                        apprehension thereof in any fresher or any other student. </p>
                    <p>
                        c) Asking any student to do any act which such student will not in the ordinary course do and
                        which has the effect of causing or generating a sense of shame, or torment or embarrassment so
                        as to adversely affect the physique or psyche of such fresher or any other student. </p>
                    <p>
                        d) Any act by a senior student that prevents, disrupts or disturbs the regular academic activity
                        of any other student or a fresher. </p>
                    <p>
                        e) Exploiting the services of a fresher or any other student for completing the academic tasks
                        assigned to an individual or a group of students.</p>
                    <p>
                        f) Any act of financial extortion or forceful expenditure burden put on a fresher or any other
                        student by students </p>
                    <p>
                        g) Any act of physical abuse including all variants of it: sexual abuse, homosexual assaults,
                        stripping, forcing obscene and lewd acts, gestures, causing bodily harm or any other danger to
                        health or person; </p>
                    <p>
                        h) Any act or abuse by spoken words, emails, post, public insults which would also include
                        deriving perverted pleasure, vicarious or sadistic thrill from actively or passively
                        participating in the discomfiture to fresher or any other student. </p>
                    <p>
                        i) Any act that affects the mental health and self-confidence of a fresher or any other student
                        with or without an intent to derive a sadistic pleasure or showing off power, authority or
                        superiority by a student over any fresher or any other student.</p>


                    <h3>
                        <p class="title">As per Hon’ble Supreme Court of India’s Order</p>
                    </h3>
                    <p>
                        “If any incident of ragging comes to the notice of the authority, the concerned student shall be
                        given liberty to explain and if his/her explanation is not found satisfactory, the authority
                        would expel him/her.” </p>
                    <p>
                        1. Accordingly, ragging is illegal and completely prohibited and banned both inside and outside
                        the Institute, hostel or campus.</p>
                    <p>

                        2. If any student is caught or named by the victim student or his/her parents/guardians, an FIR
                        will be lodged against the student (culprit).</p>
                    <p>

                        3. Strict action leading to termination of admission and rustication from the institution would
                        be taken against any ragging complaint and decision of the Management would be final &amp;
                        binding to the student(s).</p>
                    <p>

                        4. Parent/Guardians are also advised to counsel their ward to neither engage in any sort of
                        ragging activity nor incite any other student to do so.
                    </p>
                    <h3>Anti-Ragging Committee </h3>
                    <p class="title">The Members include:</p>
                    <p>
                        1. Chairman of the Institute</p>
                    <p>
                        2. Member of the Trust </p>
                    <p>
                        3. Principal </p>
                    <p>
                        4. Senior Faculty Member</p>
                    <p>
                        5. Warden</p>
                    <p>
                        At the time of admission, all students have to give an undertaking, countersigned by their
                        guardian, regarding their ant-ragging commitments.
